package Clases;

/**
 *
 * @author PC
 */
public class DetallesBebida {
    private String nombre;
    private double precio;

    public DetallesBebida() {
    }

    
    
    public DetallesBebida(String nombre, double precio) {
        this.nombre = nombre;
        this.precio = precio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
    @Override
    public String toString() {
        return "Nombre: " + nombre + ", Precio: " + precio;
    }
}
