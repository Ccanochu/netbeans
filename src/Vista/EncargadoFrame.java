     private void cargarDatos() {
        // Utiliza el DAO para obtener una lista de encargados de entrega
        List<EncargadoEntrega> listaEncargados = encargadoDAO.getEncargadoEntrega();

        // Llena tu tabla con los datos de la lista
         DefaultTableModel model = (DefaultTableModel) tbEncargados.getModel();
        model.setRowCount(0); // Limpia la tabla

        for (EncargadoEntrega encargado : listaEncargados) {
            Object[] fila = {encargado.getID_Encargado(), encargado.getNombre()};
            model.addRow(fila);
        }
        
        tbEncargados.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
               cargarDatosSeleccionados();
            }
        });
    }
     
    

