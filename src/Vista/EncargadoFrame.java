    public void guardarDatos() {
    // Obtener los datos de la interfaz
        String nombre = txtName.getText(); // Suponiendo que txtNombre es un JTextField

        // Validar que los datos estén completos (puedes agregar más validaciones según tu aplicación)
        if (nombre.isEmpty()) {
            
            return; // Sale del método si los datos no están completos
        }

        // Crear un objeto EncargadoEntrega con los datos
        EncargadoEntrega encargado = new EncargadoEntrega();
        encargado.setNombre(nombre);

        // Guardar el encargado usando el ADO
        encargadoDAO.saveEncargadoEntrega(encargado);
        
    }

    // Método para cargar los datos del elemento seleccionado en la tabla a los campos de edición
    private void cargarDatosSeleccionados() {
        int filaSeleccionada = tbEncargados.getSelectedRow();
        if (filaSeleccionada >= 0) {
            // Obtener la ID y el nombre de la fila seleccionada
            int idEncargado = Integer.parseInt(tbEncargados.getValueAt(filaSeleccionada, 0).toString());
            String nombre = tbEncargados.getValueAt(filaSeleccionada, 1).toString(); // Suponiendo que el nombre está en la segunda columna

            // Mostrar la ID y el nombre en los campos correspondientes
            txtID.setText(String.valueOf(idEncargado)); // Suponiendo que txtID es un JTextField
            txtName.setText(nombre);
        }
    }


    // Método para actualizar los datos
    // Método para actualizar los datos
    private void actualizarDatos() {
       
       
        String nuevonombre = txtName.getText(); // Suponiendo que txtNombre es un JTextField

        // Validar que los datos estén completos
        if (nuevonombre.isEmpty() || txtID.getText().isEmpty()) {
            return; // Sal del método si los datos no están completos o no hay ID
        }

        // Obtener el ID como un entero
        int idEncargado = Integer.parseInt(txtID.getText());

        // Crear un objeto EncargadoEntrega con los datos
        EncargadoEntrega encargado = new EncargadoEntrega();
        encargado.setNombre(nuevonombre);

        // Guardar el encargado usando el ADO
        encargadoDAO.updateEncargadoEntrega(encargado, idEncargado);
    }

    private void eliminarDatos(){

        String idEncargado = txtID.getText();

        encargadoDAO.deleteEncargadoEntrega(Integer.parseInt(idEncargado));
   }


