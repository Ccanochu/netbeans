    private void cargarNumerosDeMesero() {
        // Utiliza el MeseroDAO para obtener los n√∫meros de mesero
        List<Integer> numerosMesero = meseroDAO.obtenerNumerosDeMesero();

        // Llena tu tabla con los datos obtenidos
        DefaultTableModel model = (DefaultTableModel) tbNumMeseros.getModel();
        model.setRowCount(0); // Limpia la tabla

        for (Integer numero : numerosMesero) {
            Object[] fila = {numero};
            model.addRow(fila);
        }
    }


